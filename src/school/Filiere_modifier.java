    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
int selectedRow = jTable1.getSelectedRow();
if (selectedRow != -1) { // If a row is selected
    // Get the ID of the selected row directly from the jTable
    int id = (int) model.getValueAt(selectedRow, 0);
    // Get the updated values from the text fields
    String nom = txtNom.getText();
    String annee_accreditationString = txtYears.getText();
    String coordonnateur = txtCoord.getText();

    // Convert year string to a valid date format
    Date annee_accreditation;
    try {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy");
        java.util.Date parsedDate = dateFormat.parse(annee_accreditationString);
        annee_accreditation = new Date(parsedDate.getTime());
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(null, "Invalid year format", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method if the year format is invalid
    }

    // Update the record in the database
    String query = "UPDATE filiere SET Nom=?, annee_accreditation=?, coordonnateur=? WHERE id=?";
    try (Connection conn = ConnectionDB.getConnection();
         PreparedStatement ps = conn.prepareStatement(query)) {
        ps.setString(1, nom);
        ps.setDate(2, annee_accreditation);
        ps.setString(3, coordonnateur);
        ps.setInt(4, id);
        int updatedRows = ps.executeUpdate();
        if (updatedRows > 0) {
            JOptionPane.showMessageDialog(null, "Filiere updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            // Refresh the table
            loadData();
        } else {
            JOptionPane.showMessageDialog(null, "Failed to update filiere", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "An error occurred while updating filiere: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
} else {
    JOptionPane.showMessageDialog(null, "Please select a row to modify", "Warning", JOptionPane.WARNING_MESSAGE);
}
    }//GEN-LAST:event_btnModifierActionPerformed

