     private void loadData() {
        model.setRowCount(0); // Clear existing data from the table
        String query = "SELECT * FROM filiere";
        try (Connection conn = ConnectionDB.getConnection();
             PreparedStatement ps = conn.prepareStatement(query);
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                int id = rs.getInt("id");
                String nom = rs.getString("Nom");
                String annee_accreditation = rs.getString("annee_accreditation");
                String coordonnateur = rs.getString("coordonnateur");

                // Add each row to the table model
                model.addRow(new Object[]{id, nom,annee_accreditation,coordonnateur});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "An error occurred while loading data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
     
      private class TableSelectionListener implements ListSelectionListener {
        @Override
        public void valueChanged(ListSelectionEvent e) {
            if (!e.getValueIsAdjusting()) {
                // Get selected row data
                int selectedRow = jTable1.getSelectedRow();
                if (selectedRow != -1) { // If a row is selected
                    String nom = model.getValueAt(selectedRow, 1).toString();
                    String annee_accreditation = model.getValueAt(selectedRow, 2).toString();
                    String coordonnateur = model.getValueAt(selectedRow, 3).toString();

                    // Set text fields with selected row data
                    txtNom.setText(nom);
                    txtYears.setText(annee_accreditation);
                    txtCoord.setText(coordonnateur);
                }
            }
        }
    }
