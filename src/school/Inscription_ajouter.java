    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
       int selectedStudentRow = jTable1.getSelectedRow();
    int selectedFiliereRow = jTable2.getSelectedRow();

    if (selectedStudentRow == -1 || selectedFiliereRow == -1) {
        JOptionPane.showMessageDialog(null, "Veuillez sélectionner un étudiant et une filière.", "Sélection manquante", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int studentId = (int) jTable1.getValueAt(selectedStudentRow, 0); // Assuming ID is the first column
    int filiereId = (int) jTable2.getValueAt(selectedFiliereRow, 0); // Assuming ID is the first column

    if (!isInscriptionExist(studentId, filiereId)) {
        addInscription(studentId, filiereId);
    } else {
        JOptionPane.showMessageDialog(null, "L'inscription existe déjà.", "Duplication", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_jButton8ActionPerformed

