private boolean isInscriptionExist(int studentId, int filiereId) {
    String query = "SELECT COUNT(*) FROM inscription WHERE id_etudiant = ? AND id_filiere = ?";
    try (Connection conn = ConnectionDB.getConnection();
         PreparedStatement ps = conn.prepareStatement(query)) {
        ps.setInt(1, studentId);
        ps.setInt(2, filiereId);
        try (ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                return rs.getInt(1) > 0;
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Une erreur s'est produite lors de la vérification de l'inscription: " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    return false;
}

    private void loadStudentData(String studentId) {
    studentModel.setRowCount(0); // Clear existing data from the table
    String query = "SELECT * FROM students WHERE student_id = ?";
    try (Connection conn = ConnectionDB.getConnection();
         PreparedStatement ps = conn.prepareStatement(query)) {
        ps.setString(1, studentId);
        try (ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                int id = rs.getInt("student_id");
                String nom = rs.getString("first_name");
                String prenom = rs.getString("last_name");
                String dateNaissance = rs.getString("age");
                String email = rs.getString("email");
                String sexe = rs.getString("sexe");
                // Add the row to the table model
                studentModel.addRow(new Object[]{id, nom, prenom, dateNaissance, email, sexe});
            } else {
                JOptionPane.showMessageDialog(null, "Aucun étudiant trouvé avec cet ID.", "Aucun résultat", JOptionPane.INFORMATION_MESSAGE);
                clearTable(jTable1);
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "An error occurred while loading student data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

private void clearTable(JTable table) {
    DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0);
}

    private void loadFiliereData(String filiereName) {
    filiereModel.setRowCount(0); // Clear existing data from the table
    String query = "SELECT * FROM filiere WHERE nom = ?";
    try (Connection conn = ConnectionDB.getConnection();
         PreparedStatement ps = conn.prepareStatement(query)) {
        ps.setString(1, filiereName);
        try (ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                int id = rs.getInt("id");
                String nom = rs.getString("nom");
                String annee_accreditation = rs.getString("annee_accreditation");
                String coordonnateur = rs.getString("coordonnateur");
                // Add each row to the table model
                filiereModel.addRow(new Object[]{id, nom, annee_accreditation, coordonnateur});
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "An error occurred while loading filiere data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}


private void addInscription(int studentId, int filiereId) {
    String query = "INSERT INTO inscription (id_etudiant, id_filiere, date_inscription) VALUES (?, ?, ?)";
    LocalDate currentDate = LocalDate.now();
    try (Connection conn = ConnectionDB.getConnection();
         PreparedStatement ps = conn.prepareStatement(query)) {
        ps.setInt(1, studentId);
        ps.setInt(2, filiereId);
        ps.setDate(3, Date.valueOf(currentDate)); // Convert LocalDate to java.sql.Date
        int rowsAffected = ps.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Inscription ajoutée avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Échec de l'ajout de l'inscription.", "Échec", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Une erreur s'est produite lors de l'ajout de l'inscription: " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
    }
}
