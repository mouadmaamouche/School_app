    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
            // Get the selected item from the combo box
  String selectedItem = txtID.getText();

  // Prepare the query based on the selected item
  String query;
  if (selectedItem.isEmpty()) {
    // Handle empty selection (optional)
    query = "SELECT * FROM enseignant"; // Show all enseignants if nothing is selected
  } else {
    query = "SELECT * FROM enseignant WHERE id = ?";
  }

  try (Connection conn = ConnectionDB.getConnection();
       PreparedStatement ps = conn.prepareStatement(query)) {
    if (!selectedItem.isEmpty()) {
      ps.setString(1, selectedItem);
    }
    try (ResultSet rs = ps.executeQuery()) {
      // Clear existing data from the table model (optional)
      model.setRowCount(0);

      while (rs.next()) {
        int id = rs.getInt("id"); // Assuming you want to display the id
        String nom = rs.getString("nom");
        String prenom = rs.getString("prenom");
        String specialite = rs.getString("specialite");
        String coordonnees = rs.getString("coordonnees");

        // Add each row to the table model
        model.addRow(new Object[]{id, nom, prenom, specialite, coordonnees});
      }
      // Optional: Show a message if no results found
      if (model.getRowCount() == 0) {
        JOptionPane.showMessageDialog(null, "No results found for the selected enseignant!");
      }
    }
  } catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "An error occurred while searching: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
  }
    }//GEN-LAST:event_btnSearchActionPerformed

