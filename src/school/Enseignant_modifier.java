    private void loadData() {
    model.setRowCount(0); // Clear existing data from the table
    String query = "SELECT * FROM enseignant ";
    try (Connection conn = ConnectionDB.getConnection();
         PreparedStatement ps = conn.prepareStatement(query)) { // Set the parameter value
        try (ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                int id = rs.getInt("id");
                String nom = rs.getString("nom");
                String Prenom = rs.getString("Prenom");
                String Specialite = rs.getString("Specialite");
                String coordonnees = rs.getString("coordonnees");
                // Add each row to the table model
                model.addRow(new Object[]{id, nom,Prenom,Specialite, coordonnees});
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "An error occurred while loading data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

